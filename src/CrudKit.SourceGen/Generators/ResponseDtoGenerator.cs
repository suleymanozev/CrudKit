using System.Collections.Generic;
using System.Text;
using CrudKit.SourceGen.Models;

namespace CrudKit.SourceGen.Generators;

/// <summary>
/// Generates a Response DTO positional record using the configured naming convention.
/// Always emitted — even for ReadOnly entities.
/// Excludes properties marked with [SkipResponse].
/// Includes system fields (Id, CreatedAt, UpdatedAt) and optional soft-delete/tenant fields.
/// </summary>
internal static class ResponseDtoGenerator
{
    public static string Generate(EntityMetadata entity, NamingConvention naming)
    {
        // Start with system fields that belong in the response
        var responseProps = new List<(string Type, string Name)>
        {
            ("Guid", "Id"),
            ("DateTime", "CreatedAt"),
            ("DateTime", "UpdatedAt"),
        };

        if (entity.ImplementsISoftDeletable)
            responseProps.Add(("DateTime?", "DeletedAt"));

        if (entity.ImplementsIMultiTenant)
            responseProps.Add(("string", "TenantId"));

        // Add user-defined properties, excluding [SkipResponse]
        // Also collect additional usings from property types (e.g. enum namespaces)
        var additionalUsings = new HashSet<string>();
        foreach (var p in entity.Properties)
        {
            if (p.IsSkipResponse)
                continue;

            string typeName = p.IsNullable && !p.TypeName.EndsWith("?")
                ? p.TypeName + "?"
                : p.TypeName;

            responseProps.Add((typeName, p.Name));

            if (p.FullTypeName != p.TypeName && p.FullTypeName.Contains("."))
            {
                var ns = p.FullTypeName.Substring(0, p.FullTypeName.LastIndexOf('.'));
                if (!ns.StartsWith("System") && ns != entity.Namespace)
                    additionalUsings.Add(ns);
            }
            // Also handle nullable enum types: Namespace.EnumType? → Namespace
            var cleanFullType = p.FullTypeName.TrimEnd('?');
            var cleanTypeName = p.TypeName.TrimEnd('?');
            if (cleanFullType != cleanTypeName && cleanFullType.Contains("."))
            {
                var ns = cleanFullType.Substring(0, cleanFullType.LastIndexOf('.'));
                if (!ns.StartsWith("System") && ns != entity.Namespace)
                    additionalUsings.Add(ns);
            }
        }

        string className = naming.FormatResponseDtoName(entity.Name);

        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("// Generated by CrudKit.SourceGen — do not edit manually.");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using System;");
        foreach (var ns in additionalUsings)
            sb.AppendLine($"using {ns};");
        sb.AppendLine();
        sb.AppendLine($"namespace {entity.Namespace}.Dtos;");
        sb.AppendLine();
        sb.Append($"public sealed record {className}(");
        sb.AppendLine();

        for (int i = 0; i < responseProps.Count; i++)
        {
            var (type, name) = responseProps[i];
            bool isLast = i == responseProps.Count - 1;

            sb.Append($"    {type} {name}");
            if (!isLast)
                sb.AppendLine(",");
            else
                sb.AppendLine(");");
        }

        return sb.ToString();
    }
}
